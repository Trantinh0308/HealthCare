package com.example.healthcare.models;

public class Repeat {
    String time;
    String date;

    public Repeat() {
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
