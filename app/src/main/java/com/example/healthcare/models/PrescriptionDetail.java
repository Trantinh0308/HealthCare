package com.example.healthcare.models;

public class PrescriptionDetail {
    String time;
    String note;
    public PrescriptionDetail() {
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
